
public class MathSinifi {
	public static void main(String[] args) {
		//Math sýnýfý metotlari
		//Math.ceil(x)=Bir buyuk tam sayýya yuvarlama yapar
		//Math.cos(x)=cos degerini doner.
		//Math.sin(x)=sin degerini doner.
		//Math.floor(x)=Kucuk tam sayýya yuvarlar
		//Math.max(x,y)=Buyuk tam sayýyý verir
		//Math.min(x,y)=Kucuk tam sayýy verir
		//Math.pow(3,5)=3^5 ustunu alýr
		//Math.random()=1 ve 0 arasýnda rastgele sayi ureti
		//Math.round(x)=yakýn olan tam sayýya doner
		//Math.abs(x)=Mutlak deger
		//Math.sqrt(x)=karekok alýr
		System.out.println(Math.ceil(1.1));
		System.out.println(Math.cos(30));
		System.out.println(Math.floor(1.1));
		System.out.println(Math.max(1.2,1));
		System.out.println(Math.pow(2,3));
		System.out.println(Math.round(1.1));
		System.out.println(Math.abs(-1));
		System.out.println(Math.sqrt(16));
		//hepsi double veri tipi tabi isterseniz typecasting iþlemi ile tipini degistirebilirsiniz.
	}

}
